import turtle
sam = turtle.Turtle()

sam.color("blue","cyan")
sam.begin_fill()
sam.forward(100)
sam.left(90)
sam.forward(100)
sam.left(90)
sam.forward(100)
sam.left(90)
sam.forward(100)

sam.penup()
sam.forward(10)
sam.pendown()

sam.forward(100)
sam.left(90)
sam.forward(100)
sam.left(90)
sam.forward(100)
sam.left(90)
sam.forward(100)
sam.end_fill()



turtle.done()